from sympy import symbols, Eq, solve, sqrt

a1 = symbols("aa")
b1 = symbols("ba")
a2 = symbols("ab")
b2 = symbols("bb")
a3 = symbols("ac")
b3 = symbols("bc")
x = symbols("x")
y = symbols("y")
z = symbols("z")
#eq1 = Eq(368.4, sqrt((a1**2)+(b1**2)))
eq1 = Eq(x, sqrt((a1**2)+(b1**2)))
#eq2 = Eq(260.4, sqrt((a2**2)+(b2**2)))
